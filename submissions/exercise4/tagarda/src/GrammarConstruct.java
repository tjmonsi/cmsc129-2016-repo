class GrammarConstruct {
/*
*GRAMMAR CONSTRUCT:
*A GRAMMAR CONSTRUCT IS A terminal / nonterminal in a particular grammar
* e.g make, purr, VarID, Integer..
*/
        String construct;
        int type;
        public GrammarConstruct(String construct, int type) {
            this.construct = construct;
            this.type = type;
        }
}
